valor = int(input("Insira um número: "))

dobro = valor * 2
triplo = valor * 3
quadrado = valor *4
raiz = valor **(1/2)
raiz_cubica = valor **(1/3)

print(f"O dobro de {valor} é :{dobro}".format(valor,dobro))
print(f"O triplo de {valor} é :{triplo}".format(valor,triplo))
print(f"O quadrado de {valor} é :{quadrado}".format(valor,quadrado))
print(f"A raiz quadrada de {valor} é :{raiz}".format(valor,raiz))
print(f"A raiz cubica de {valor} é :{raiz_cubica}".format(valor,raiz_cubica))